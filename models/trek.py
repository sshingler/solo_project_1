class Trek: 
    def __init__(self, trek_name, trek_distance, trek_days, trek_headline, trek_completed, trek_notes, destination, id = None):
        self.trek_name = trek_name
        self.trek_distance = trek_distance
        self.trek_days = trek_days
        self.trek_headline = trek_headline
        self.trek_completed = trek_completed
        self.trek_notes = trek_notes
        self.destination = destination 
